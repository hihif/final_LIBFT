#include"libft.h"

int	ft_isalnum(int c)
{
	unsigned char	c1;

	c1 = c;
	if ((c1 >= 'a' && c1 <= 'z') || (c1 >= 'A' && c1 <= 'Z') \
	|| (c1 >= '0' && c1 <= '9'))
		return (1);
	return (0);
}
